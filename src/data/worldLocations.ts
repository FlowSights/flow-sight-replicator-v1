// Helper function to search locations
export const searchLocations = (query: string): typeof worldLocations => {
  if (!query || query.length === 0) return worldLocations.slice(0, 10);
  
  const lowerQuery = query.toLowerCase();
  const results = worldLocations
    .filter(loc => 
      loc.label.toLowerCase().includes(lowerQuery) ||
      loc.value.toLowerCase().includes(lowerQuery)
    );
    
  // Prioritize countries over cities, then sort by length to prioritize exact matches
  results.sort((a, b) => {
    if (a.type === 'country' && b.type !== 'country') return -1;
    if (a.type !== 'country' && b.type === 'country') return 1;
    return a.label.length - b.label.length;
  });

  return results.slice(0, 15); // Limit results to 15 for performance
};
